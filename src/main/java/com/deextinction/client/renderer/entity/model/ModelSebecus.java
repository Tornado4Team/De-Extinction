				rfoot = this.getRotateAngleComplexInclinated(time, walkSpeed, animSpeed, 0.3F, -1.5707963268F, 0.2F);

				lleg1 = this.getRotateAngleComplexInclinated(time, walkSpeed, animSpeed, 0.8F, 0.0F, 0.1F);
				lleg3 = this.getRotateAngleComplexInclinated(time, walkSpeed, animSpeed, 0.6F, 1.5707963268F, -0.4F);
				lfoot = this.getRotateAngleComplexInclinated(time, walkSpeed, animSpeed, 0.3F, 1.5707963268F, 0.2F);

				this.rleg_left1.rotateAngleX += lleg1;
				this.rleg_left2.rotateAngleX += lleg2;
